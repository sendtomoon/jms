package com.jy.entity.scm;

public class MaterialVO {

	private String stoneshape;//石形
	private String clartity;//净度
	private String color;//颜色
	private String cut;//切工
	private Double weight;//重量
	private Double purcost;//采购成本
	private String cernum;//证书号
	private String certype;//证书类型
	public String getStoneshape() {
		return stoneshape;
	}
	public void setStoneshape(String stoneshape) {
		this.stoneshape = stoneshape;
	}
	public String getClartity() {
		return clartity;
	}
	public void setClartity(String clartity) {
		this.clartity = clartity;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	public String getCut() {
		return cut;
	}
	public void setCut(String cut) {
		this.cut = cut;
	}
	public Double getWeight() {
		return weight;
	}
	public void setWeight(Double weight) {
		this.weight = weight;
	}
	public Double getPurcost() {
		return purcost;
	}
	public void setPurcost(Double purcost) {
		this.purcost = purcost;
	}
	public String getCernum() {
		return cernum;
	}
	public void setCernum(String cernum) {
		this.cernum = cernum;
	}
	public String getCertype() {
		return certype;
	}
	public void setCertype(String certype) {
		this.certype = certype;
	}
	
	
}
